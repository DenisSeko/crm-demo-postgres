import axios from "axios"

// ‚≠ê‚≠ê‚≠ê NETLIFY OPTIMIZED - koristi relative paths ‚≠ê‚≠ê‚≠ê
const API_URL = ""  // Prazno za relative paths + Netlify redirects

console.log('üöÄ API Service initialized for Netlify deployment')
console.log('üìç Using relative paths with Netlify redirects')

const api = axios.create({
  baseURL: API_URL,
  headers: {
    "Content-Type": "application/json",
  },
})

// Add detailed request logging
api.interceptors.request.use((config) => {
  const fullUrl = config.baseURL + config.url
  console.log('üì° Making API request to:', fullUrl)
  console.log('üîß Request details:', {
    method: config.method?.toUpperCase(),
    endpoint: config.url,
    fullURL: fullUrl,
    hasData: !!config.data
  })
  return config
})

// Add response logging
api.interceptors.response.use(
  (response) => {
    console.log('‚úÖ API Response success:', {
      endpoint: response.config.url,
      status: response.status,
      data: response.data
    })
    return response
  },
  (error) => {
    console.error('‚ùå API Error occurred:', {
      url: error.config?.url,
      status: error.response?.status,
      message: error.response?.data?.message || error.message,
      fullError: error.response?.data
    })
    return Promise.reject(error)
  }
)

// Auth API
export const authAPI = {
  login: async (email, password) => {
    console.log('üîê Auth login initiated for:', email)
    const response = await api.post("/api/login", { email, password })
    console.log('üîë Login response received')
    return response.data
  },
}

